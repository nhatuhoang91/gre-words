public class StatsObject {
    private String date;
    private int time;
    private int view;

    public StatsObject() {

    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDate() {
        return this.date;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getTime() {
        return this.time;
    }

    public void setView(int view) {
        this.view = view;
    }

    public int getView() {
        return this.view;
    }
}